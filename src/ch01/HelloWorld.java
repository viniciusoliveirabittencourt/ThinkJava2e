package ch01;

// this is called class, and it's a sequence of methods
// it's good a class start with a capital letter
// must have the same file name, so file and class are called HelloWorld
public class HelloWorld {

    // this is called method, and it's a sequence of instructions
    // a main method in java it's especial, because every program starts with it and end with it
    public static void main(String[] args) {

        // generates a simple output
        // this line is an instruction, more specific, a print instruction
        System.out.println("Hello World!");
        // java is case-sensitive, so System != system != SYSTEM
    }
}
