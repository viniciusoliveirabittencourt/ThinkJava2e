package ch01;

public class HelloWorld3 {
    public static void main(String[] args) {
        // its possible use escape sequences to make multiple lines with the escape sequence "\n"
        System.out.print("Hello!\nHow are you?\n");

        // another escape sequences

        // tab "\t"
        System.out.println("\tHello World!");

        // double quote '\"'
        System.out.println("She said \"hello\" to me!");

        // blackslash
        System.out.println("You need to use \\n to break a line!");
    }
}
